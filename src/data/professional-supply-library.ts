/**
 * Professional Supply Library
 * 
 * Curated reference dataset of salon chemical/supply product lines
 * organized by brand. Used by the Backroom Product Catalog to let
 * salon owners quickly build their supply inventory.
 * 
 * Every individual shade / volume variant is listed separately so
 * each tube, bottle, or container can be tracked independently.
 */

export interface SupplyLibraryItem {
  brand: string;
  name: string;
  category: 'color' | 'lightener' | 'developer' | 'toner' | 'bond builder' | 'treatment' | 'additive';
  defaultDepletion: 'weighed' | 'per_service' | 'manual' | 'per_pump';
  defaultUnit: 'g' | 'ml' | 'oz';
  sizeOptions?: string[];
}

// ─── Helper to generate shade entries ───────────────────────────
function c(brand: string, name: string, category: SupplyLibraryItem['category'], sizeOptions: string[], depletion: SupplyLibraryItem['defaultDepletion'] = 'weighed', unit: SupplyLibraryItem['defaultUnit'] = 'g'): SupplyLibraryItem {
  return { brand, name, category, defaultDepletion: depletion, defaultUnit: unit, sizeOptions };
}

